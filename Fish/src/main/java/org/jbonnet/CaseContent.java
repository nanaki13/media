package org.jbonnet;

public class CaseContent {

	private Constants.Deep deep;
	
	private Fish fish;
	private Pecheur pecheur;
	/**
	 * @return the deep
	 */
	public Constants.Deep getDeep() {
		return deep;
	}
	/**
	 * @param deep the deep to set
	 */
	public void setDeep(Constants.Deep deep) {
		this.deep = deep;
	}
	/**
	 * @return the fish
	 */
	public Fish getFish() {
		return fish;
	}
	/**
	 * @param fish the fish to set
	 */
	public void setFish(Fish fish) {
		this.fish = fish;
	}
	/**
	 * @return the pecheur
	 */
	public Pecheur getPecheur() {
		return pecheur;
	}
	/**
	 * @param pecheur the pecheur to set
	 */
	public void setPecheur(Pecheur pecheur) {
		this.pecheur = pecheur;
	}
	
}
